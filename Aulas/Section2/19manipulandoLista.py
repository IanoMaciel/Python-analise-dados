# Manipulando Listas
#   1. append() -> para adicionar um item no final da lista 
#   2. len() -> calcula o TAM da lista 
#   3. [] -> acessa o indice 
#   4. del() -> excluir um elemento 
#   5. clear() -> limpar a lista 
#   6. insert() -> inserir um item na lista em um indice específico 
#   7. extend() -> anexar elemento de outra lista na lista atual
#   8. remove() -> remover um item especifico
#   9. pop() -> remove o indice especificado 
#   10. sort() -> ordena os valores
#   11. copy() -> faz uma cópia da lista
#   12. index() -> retona o index do elemento da lista 

